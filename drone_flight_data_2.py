# Второй пример программы, сделанный с учетом замечания преподавателя
# > Сильно подробно можно не описывать. Из примера важно представить, что у Вас БЛА, 
# передал определённые данные по времени, высоте, скорости и т.д. и, допустим на высоте 
# 320 метров произошла неполадка или потеря высоты. Необходимо определить в какое время 
# это произошло, по GPS проверить, где БЛА находился в это время и проанализировать, 
# что могло повлиять.
#
#
#
#
# Данные о полёте БЛА: время, высота (в метрах), скорость (в км/ч), GPS координаты.
flight_data = [
    {"время": "2024-10-10 14:00", "высота": 350, "скорость": 60, "GPS": (55.7558, 37.6173)},
    {"время": "2024-10-10 14:05", "высота": 340, "скорость": 62, "GPS": (55.7550, 37.6170)},
    {"время": "2024-10-10 14:10", "высота": 330, "скорость": 65, "GPS": (55.7548, 37.6165)},
    {"время": "2024-10-10 14:15", "высота": 320, "скорость": 67, "GPS": (55.7545, 37.6160)},
    {"время": "2024-10-10 14:20", "высота": 310, "скорость": 70, "GPS": (55.7540, 37.6155)},  # Потеря высоты
    {"время": "2024-10-10 14:25", "высота": 300, "скорость": 75, "GPS": (55.7535, 37.6150)},  # Потеря высоты
]

# Определяем критическую высоту (например, 320 метров)
critical_height = 320

# Функция для анализа данных о потере высоты
def analyze_flight_data(flight_data, critical_height):
    for record in flight_data:
        if record["высота"] <= critical_height:
            print(f"Проблема с высотой! Высота упала до {record['высота']} метров.")
            print(f"Время: {record['время']}")
            print(f"Координаты GPS: {record['GPS']}")
            
            # Анализ возможных причин (пример простого анализа)
            if record["скорость"] > 70:
                print(f"Возможно, проблема вызвана слишком высокой скоростью ({record['скорость']} км/ч).")
            else:
                print(f"Необычное снижение высоты. Возможные причины: погодные условия или техническая неисправность.")
            print()  # Пустая строка для разделения

# Запускаем анализ данных
analyze_flight_data(flight_data, critical_height)
